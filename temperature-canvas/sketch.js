function draw() {
    background(0); //set background on grey scale

    for (let i = 0; i < tempsOfSeattle.length; i++) {
        chain(i, colorScheme(tempsOfSeattle[i]));
    }
}

function chain(yAxis, color) {
    strokeWeight(lineWidth);  //border

    //set colors of chain border
    stroke(color[0],color[1],color[2]);
    noFill();
    for (let i = 0; i <= width ; i += 8) {
        circle(i, yAxis, 5);
    }
}

function colorScheme(temp) {
    if(temp < 10) {
        return  [100,100,255];
    }
    if(temp < 20) {
        return  [100,100,220];
    }
    if(temp < 30) {
        return  [100,120,200];
    }
    if(temp < 40) {
        return  [140,120,190];
    }
    if(temp < 50) {
        return  [150,110,160];
    }
    if(temp < 60) {
        return  [170,100,120];
    }
    if(temp < 70) {
        return  [200,100,100];
    }
    return [0,0,0]
}